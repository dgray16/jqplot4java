package br.com.digilabs.jqplot.elements;

public class TableLegendRenderer implements Element
{

	private static final long serialVersionUID = 8595187092890943392L;
	
	private String background;
	private String border;
	private String fontSize;
	private String fontFamily;
	private String textColor;
	private String marginTop;
	private String marginBotton;
	private String marginLeft;
	private String marginRight;
	
	public String getBackground()
	{
		return background;
	}
	public void setBackground(String background)
	{
		this.background = background;
	}
	public String getBorder()
	{
		return border;
	}
	public void setBorder(String border)
	{
		this.border = border;
	}
	public String getFontSize()
	{
		return fontSize;
	}
	public void setFontSize(String fontSize)
	{
		this.fontSize = fontSize;
	}
	public String getFontFamily()
	{
		return fontFamily;
	}
	public void setFontFamily(String fontFamily)
	{
		this.fontFamily = fontFamily;
	}
	public String getTextColor()
	{
		return textColor;
	}
	public void setTextColor(String textColor)
	{
		this.textColor = textColor;
	}
	public String getMarginTop()
	{
		return marginTop;
	}
	public void setMarginTop(String marginTop)
	{
		this.marginTop = marginTop;
	}
	public String getMarginBotton()
	{
		return marginBotton;
	}
	public void setMarginBotton(String marginBotton)
	{
		this.marginBotton = marginBotton;
	}
	public String getMarginLeft()
	{
		return marginLeft;
	}
	public void setMarginLeft(String marginLeft)
	{
		this.marginLeft = marginLeft;
	}
	public String getMarginRight()
	{
		return marginRight;
	}
	public void setMarginRight(String marginRight)
	{
		this.marginRight = marginRight;
	}
	
}
